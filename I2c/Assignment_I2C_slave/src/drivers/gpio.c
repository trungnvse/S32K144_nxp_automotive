#include "gpio.h"
/*******************************************************************************
* Define
*******************************************************************************/
void GPIO_Config(GPIO_Type* base, uint8_t pin, gpio_config_t* config)
{
	/*Output*/
	if(config->direction == Output)
	{
		/*configuration GPIO to output*/
		base->PDDR |= (1<<pin);
		/*initialize default state*/
		GPIO_Write(base, pin, config->logic);
	}

	/*Input*/
}

