#include "I2c_config.h"
#include "Lpit_config.h"
#include "pin_config.h"
#include "clock_config.h"
/*******************************************************************************
* Code
*******************************************************************************/
int main(void)
{
	BOARD_InitClock();
	BOARD_InitPins();
	BOARD_InitI2c();
	BOARD_InitLpit();

    /* Enable Global Interrupts */
    __asm volatile ("cpsie i");

    for(;;)
    {
    	if(flat_change)
    	{
       		change_adc();
    	}

    }

    return 0;
}
