#include "systick_config.h"
/*******************************************************************************
* Global variable
*******************************************************************************/
callback g_callback_func;
uint8_t g_counter_pwm;
/*******************************************************************************
* Code
*******************************************************************************/
void SysTick_Handler()
{
	g_counter_pwm++;
	g_callback_func(g_counter_pwm);
}

void Systick_regis_handler(callback _callback_func)
{
	g_counter_pwm = 0;
	if(_callback_func != NULL)
	{
		g_callback_func = _callback_func;
	}
}
